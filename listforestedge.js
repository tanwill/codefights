/*
-----CHALLENGE 1-----

Given an integer size, return array of length size filled with 1s.

Example

For size = 4, the output should be
createArray(size) = [1, 1, 1, 1].

SOLUTION 1:
*/

function createArray(size) {
    myArray = [];
    for(i = 0; i < size; i++){
        myArray.push(1);
    }
    return myArray;
}

